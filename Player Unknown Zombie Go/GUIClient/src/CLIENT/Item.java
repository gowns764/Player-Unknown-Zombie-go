package CLIENT;

public class Item
{
    public String name = "";
    public int power = 0;

    public void setName(String name)
    {
        this.name = name;
    }

    public void setPower(int power)
    {
        this.power = power;
    }

    public String getName()
    {
        return this.name;
    }

    public int getPower()
    {
        return this.power;
    }
}
